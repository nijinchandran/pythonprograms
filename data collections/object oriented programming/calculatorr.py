class Calculator:
    def add(self,num1,num2):
        self.num1=num1
        self.num2=num2
        print(self.num1+self.num2)

    def substract(self,num1,num2):
        self.num1=num1
        self.num2=num2
        print(self.num1-self.num2)

    def multiply(self,num1,num2):
        self.num1=num1
        self.num2=num2
        print(self.num1*self.num2)

    def division(self,num1,num2):
        self.num1=num1
        self.num2=num2
        print(self.num1/self.num2)
